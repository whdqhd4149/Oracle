--실습하기 2-1
-- 기본기(PRIMARY KEY)
-- 테이블 생성할 때 기본키 CREATE TABLE 테이블명(칼럼명 자료형 PRIMARY KEY);
CREATE TABLE USER2(
   USER_ID VARCHAR2(20) PRIMARY KEY,
   NAME VARCHAR2(20),
   HP CHAR(13),
   AGE NUMBER(2)
);
--실습하기 2-2
--고유키(UNIQUE) CREATE TABLE 테이블명(칼럼명 자료형 UNIQUE);
CREATE TABLE USER3(
    USER_ID VARCHAR2(20) PRIMARY KEY,
    NAME VARCHAR2(20),
    HP CHAR(13) UNIQUE,
    AGE NUMBER(3)
);
   
--실습하기 2-3
CREATE TABLE PARENT(
   PID VARCHAR2(20) PRIMARY KEY,
   NAME VARCHAR2(20),
   HP CHAR(13) UNIQUE
);
CREATE TABLE CHILD(
   CID VARCHAR2(20) PRIMARY KEY,
   NAME VARCHAR2(20),
   HP CHAR(13) UNIQUE,
   PARENT VARCHAR(20),
   FOREIGN KEY(PARENT)REFERENCES PARENT(PID)
);

